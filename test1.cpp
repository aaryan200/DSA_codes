ll moduloMultiplication(ll a, ll b, ll mod) {
    ll res = 0;
    a %= mod;
    while (b) {
        if (b & 1) res = (res + a) % mod;
        a = (2 * a) % mod;
        b >>= 1;
    }
    return res;
}

const ll md = 1.0e9+7;

void solve() {
    ll n;
    cin>>n;
    ll ans = n*(n-1);
    for (ll i=1;i<=n;i++) {
        ans = moduloMultiplication(ans,i,md);
    }
    print(ans);
    return;
}

